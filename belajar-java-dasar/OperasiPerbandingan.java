public class OperasiPerbandingan {
    public static void main(String[] args) {
        int a = 1;
        int b = 2;

        System.err.println(a < b);
        System.err.println(a > b);
        System.err.println(a <= b);
        System.err.println(a >= b);
        System.err.println(a == b);
        System.err.println(a != b);
    }
}
