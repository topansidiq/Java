public class TipeDataNumber {
    public static void main(String[] args) {

        /// Number
        byte thisByte = 100;
        short thisShort = 1000;
        int thisInt = 10000000;
        long thisLong = 10000000000L;

        /// Floating Point Number
        float thisFloat = 10.12F;
        double thisDouble = 1001.34;

        /// Literals
        int thisDecimal = 25;
        int thisHexadecimal = 0xA132B;
        int thisBinary = 0b11000001;

        /// Underscore
        long oneBillion = 1_000_000_000L;

    }
}
